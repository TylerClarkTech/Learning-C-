//1. Sum of Two Numbers
//		Write a program that stores the integers 50 and 100 in variables, and
//		stores the sum of these two in a variable name total.

#include <iostream>
using namespace std;

int main()
{
	int val1 = 50;
	int val2 = 100;
	int total;

	total = val1 + val2;
	cout << "The total of val1 and val2 is " << total; 

	return 0;
}
